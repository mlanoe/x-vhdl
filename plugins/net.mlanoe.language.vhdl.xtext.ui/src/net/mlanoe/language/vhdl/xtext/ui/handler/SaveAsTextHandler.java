package net.mlanoe.language.vhdl.xtext.ui.handler;

import java.io.IOException;
import java.util.Iterator;

import org.eclipse.core.commands.AbstractHandler;
import org.eclipse.core.commands.ExecutionEvent;
import org.eclipse.core.commands.ExecutionException;
import org.eclipse.core.resources.IResource;
import org.eclipse.core.runtime.IPath;
import org.eclipse.emf.common.util.URI;
import org.eclipse.emf.ecore.resource.Resource;
import org.eclipse.emf.ecore.resource.ResourceSet;
import org.eclipse.emf.ecore.resource.impl.ResourceSetImpl;
import org.eclipse.jface.viewers.ISelection;
import org.eclipse.jface.viewers.TreeSelection;
import org.eclipse.ui.IWorkbench;
import org.eclipse.ui.IWorkbenchPage;
import org.eclipse.ui.IWorkbenchWindow;
import org.eclipse.ui.PlatformUI;

import net.mlanoe.language.vhdl.Model;

public class SaveAsTextHandler extends AbstractHandler {

	public Object execute(ExecutionEvent event) throws ExecutionException {
		IWorkbench wb = PlatformUI.getWorkbench();
		IWorkbenchWindow win = wb.getActiveWorkbenchWindow();
		IWorkbenchPage page = win.getActivePage();
		ISelection selection= page.getSelection();;
		if (selection instanceof TreeSelection){
			ResourceSet rs = new ResourceSetImpl();

			for (Iterator<?> iterator = ((TreeSelection)selection).iterator(); iterator.hasNext();) {
				final Object f = (Object) iterator.next();
				if (f instanceof IResource){
						
						IPath path = ((IResource)f).getFullPath();
						URI uri = URI.createPlatformResourceURI(path.toString(), false);
						
						Resource res = rs.getResource(uri, true);
						Model target = (Model)res.getContents().get(0);
						
						URI xmiuri = res.getURI();
						URI xtxturi = xmiuri.trimFileExtension().appendFileExtension(
								"vhdl");
						
						Resource xtxtres = rs.createResource(xtxturi);
						xtxtres.getContents().add(target);
						
						try {
							xtxtres.save(null);
						} catch (IOException e) {
							e.printStackTrace();
						}
						
						res.getContents().add(target);
				}
			}

			rs.getResources().clear();

			return null;
		}
		return null;
	}

}
